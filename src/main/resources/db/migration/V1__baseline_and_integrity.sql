-- Remove the old uniqueness rule that allowed only one row per product, regardless of size.
DO $$
DECLARE object_name TEXT;
BEGIN
    FOR object_name IN
        SELECT con.conname
        FROM pg_constraint con
        JOIN pg_class rel ON rel.oid = con.conrelid
        JOIN pg_namespace ns ON ns.oid = rel.relnamespace
        WHERE ns.nspname = current_schema()
          AND rel.relname = 'saved_cart_items'
          AND con.contype = 'u'
          AND (
              REPLACE(REPLACE(pg_get_constraintdef(con.oid), ' ', ''), CHR(34), '') ILIKE '%(user_id,product_id)%'
              OR REPLACE(REPLACE(pg_get_constraintdef(con.oid), ' ', ''), CHR(34), '') ILIKE '%(product_id,user_id)%'
          )
          AND pg_get_constraintdef(con.oid) NOT ILIKE '%selected_size%'
    LOOP
        EXECUTE format('ALTER TABLE saved_cart_items DROP CONSTRAINT IF EXISTS %I', object_name);
    END LOOP;

    FOR object_name IN
        SELECT indexname
        FROM pg_indexes
        WHERE schemaname = current_schema()
          AND tablename = 'saved_cart_items'
          AND indexdef ILIKE 'CREATE UNIQUE INDEX%'
          AND (
              REPLACE(REPLACE(indexdef, ' ', ''), CHR(34), '') ILIKE '%(user_id,product_id)%'
              OR REPLACE(REPLACE(indexdef, ' ', ''), CHR(34), '') ILIKE '%(product_id,user_id)%'
          )
          AND indexdef NOT ILIKE '%selected_size%'
    LOOP
        EXECUTE format('DROP INDEX IF EXISTS %I', object_name);
    END LOOP;
END $$;

UPDATE saved_cart_items keeper
SET quantity = LEAST(merged.total_quantity, 2147483647)::INTEGER
FROM (
    SELECT MIN(id) AS keep_id,
           SUM(GREATEST(quantity, 1)::BIGINT) AS total_quantity
    FROM saved_cart_items
    GROUP BY user_id, product_id, LOWER(BTRIM(selected_size))
    HAVING COUNT(*) > 1
) merged
WHERE keeper.id = merged.keep_id;

DELETE FROM saved_cart_items duplicate
USING saved_cart_items keeper
WHERE duplicate.user_id = keeper.user_id
  AND duplicate.product_id = keeper.product_id
  AND LOWER(BTRIM(duplicate.selected_size)) = LOWER(BTRIM(keeper.selected_size))
  AND duplicate.id > keeper.id;

