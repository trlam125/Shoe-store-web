UPDATE order_item oi
SET product_name = COALESCE(NULLIF(BTRIM(oi.product_name), ''),
                            NULLIF(BTRIM(p.name), ''), 'Sản phẩm')
FROM product p
WHERE oi.product_id = p.id
  AND (oi.product_name IS NULL OR BTRIM(oi.product_name) = '');

